u are able to quantify the actual dollar value of the    damages you have suffered because of the problem.  This may be extremly difficult if its a    laneway the borrower/owner thought they had access to but do not and thus    need to use a different laneway that is further away from the property to    bring their car in. And although the original seller told the borrower/owner    that it can be used, they now discover that the neighbor is building    something over it. And yes it was only a matter of convenience to the    borrower/owner - something that cannot be necessarily quantified for title    insurance coverage - it may have been a significant inducement to purchasing    the property in the first place.  This    can now result is a lower marketability of the property reducing its value,    therefore increasing the lender's loan to value. )</td>
